package com.example.springsocial.payload;

public class AddressRequest {
	private String houseNumber;
	private String roadNumber;
	private String street;
	private String area;
	private String mandal;
	private String district;
	private String state;
	private String pincode;
	private String landMark;
	private String phoneNumber;
	private String alternatePhoneNumber;
	public final String getHouseNumber() {
		return houseNumber;
	}
	public final void setHouseNumber(String houseNumber) {
		this.houseNumber = houseNumber;
	}
	public final String getRoadNumber() {
		return roadNumber;
	}
	public final void setRoadNumber(String roadNumber) {
		this.roadNumber = roadNumber;
	}
	public final String getStreet() {
		return street;
	}
	public final void setStreet(String street) {
		this.street = street;
	}
	public final String getArea() {
		return area;
	}
	public final void setArea(String area) {
		this.area = area;
	}
	public final String getMandal() {
		return mandal;
	}
	public final void setMandal(String mandal) {
		this.mandal = mandal;
	}
	public final String getDistrict() {
		return district;
	}
	public final void setDistrict(String district) {
		this.district = district;
	}
	public final String getState() {
		return state;
	}
	public final void setState(String state) {
		this.state = state;
	}
	public final String getPincode() {
		return pincode;
	}
	public final void setPincode(String pincode) {
		this.pincode = pincode;
	}
	public final String getLandMark() {
		return landMark;
	}
	public final void setLandMark(String landMark) {
		this.landMark = landMark;
	}
	public final String getPhoneNumber() {
		return phoneNumber;
	}
	public final void setPhoneNumber(String phoneNumber) {
		this.phoneNumber = phoneNumber;
	}
	public final String getAlternatePhoneNumber() {
		return alternatePhoneNumber;
	}
	public final void setAlternatePhoneNumber(String alternatePhoneNumber) {
		this.alternatePhoneNumber = alternatePhoneNumber;
	}
	
}
